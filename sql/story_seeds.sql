-- Story Seeds: 关系史实验（Phase 1）
-- 手动写死四篇种子故事，用于验证是否比普通 Memory 更能增强人格连续性

create table if not exists story_seeds (
  id          uuid        primary key default gen_random_uuid(),
  title       text        not null,
  content     text        not null,
  importance  text        not null default 'medium',  -- high / medium / low
  themes      text[]      not null default '{}',
  enabled     boolean     not null default true,
  created_at  timestamptz not null default now()
);

-- 开放 anon 只读权限（不需要登录即可读取，与 memories 保持一致）
alter table story_seeds enable row level security;

create policy "anon can read story_seeds"
  on story_seeds for select
  using (true);

